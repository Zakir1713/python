# LITERALS EXERCISE
# string literals "Learning Python is fun"
print("Learning Python is fun!")

# integer literal and float literal
print(10, 3.14)

# using single and double quote literal
print('Single quote literal', "Double quote literal")

# boolean literal
print(True, False)

# variable with None
none_var = None
print(none_var)

# literals with type()
print(type("Hello"))
print(type(10))
print(type(3.5))
print(type(True))
print(type(None))

# string literal using ’
print("Python’s simplicity is powerful.")

# string concatenation 
print("Python " + "rocks!")

# mixed literals
mixed_list = [42, "Hello", True]
print(mixed_list)

# multiline string
print("""This is
a multi-line
string.""")

# output of None==0, and False==0
print(None == 0)      # False
print(None == False)  # False



